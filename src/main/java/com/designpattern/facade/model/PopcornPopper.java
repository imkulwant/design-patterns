package com.designpattern.facade.model;

public class PopcornPopper {

    public void on() {
        System.out.println("PopcornPopper ON");

    }

    public void off() {
        System.out.println("PopcornPopper OFF");

    }

    public void pop() {
        System.out.println("PopcornPopper POP");

    }
}
